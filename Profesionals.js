const beflexi = document.querySelector("#imageFile");
var upload_image = "";

beflexi.addEventListener("change", function() {
    const reader = new FileReader();
    reader.addEventListener("load", () => {
        upload_image = reader.result;
        document.querySelector(".pucho").style.backgroundImage = `url(${upload_image})`;
    });
    reader.readAsDataURL(this.files[0]);
})







